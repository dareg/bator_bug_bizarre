      SUBROUTINE UV2SD(U,V,S,D)

      USE PARKIND1, ONLY : JPRB
      IMPLICIT NONE

      REAL(KIND=8) :: U,V,S,D

      REAL(KIND=JPRB),PARAMETER :: DEGCON=180.0_JPRB/                   &
     &                                      3.14159265358979_JPRB
      REAL(KIND=JPRB) :: ABSU,ABSV,ADCOR,ZEPS

      ABSU=ABS(U)
      ABSV=ABS(V)
      ZEPS=10.E-6_JPRB
      IF (ABSU.GT.ZEPS) GO TO 130
  110 CONTINUE
      IF (ABSV.GT.ZEPS) GO TO 120
      S=0.0_JPRB
      D=0.0_JPRB
      RETURN
  120 CONTINUE
      S=ABSV
      D=360.0_JPRB
      IF (V.LT.0.) D=180.0_JPRB
      RETURN
  130 CONTINUE
      IF (ABSV.GT.ZEPS) GO TO 200
      S=ABSU
      D=270.0_JPRB
      IF (U.LT.0.0_JPRB) D=90.0_JPRB
      RETURN
  200 CONTINUE
      ADCOR=180.0_JPRB
      D=ATAN2(U,V)*DEGCON+ADCOR
      S=U*U+V*V
      S=SQRT(S)
      ENDSUBROUTINE UV2SD
