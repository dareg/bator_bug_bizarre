      FUNCTION ISRCHFLT(N,ARRAY,INC,TARGET)
      USE PARKIND1, ONLY : JPIM, JPRB
      IMPLICIT NONE
      INTEGER(KIND=JPIM) :: ISRCHFLT
      INTEGER(KIND=JPIM) :: N
      INTEGER(KIND=JPIM) :: INC
      REAL(KIND=JPRB) :: ARRAY(1+INC*(N-1))
      REAL(KIND=JPRB) :: TARGET
      INTEGER(KIND=JPIM) :: I
!
      IF (N.LE.0) THEN
        ISRCHFLT=0
        RETURN
      ELSE
        DO 1 I=1,N
          IF ( ARRAY(1+INC*(I-1)).LT.TARGET ) THEN
            ISRCHFLT=I
            RETURN
          ENDIF
 1       CONTINUE
        ISRCHFLT=N+1
      ENDIF
      ENDFUNCTION ISRCHFLT
